---@class Collision @Describes a collision.
---@field collider fun() @The Collider we hit (Read Only).
---@field impulse fun() @The total impulse applied to this contact pair to resolve the collision.
---@field articulationBody fun() @The ArticulationBody of the collider that your GameObject collides with (Read Only).
---@field gameObject fun() @The GameObject whose collider you are colliding with. (Read Only).
---@field rigidbody fun() @The Rigidbody we hit (Read Only). This is null if the object we hit is a collider with no rigidbody attached.
---@field transform fun() @The Transform of the object we hit (Read Only).
---@field contacts fun() @The contact points generated by the physics engine. You should avoid using this as it produces memory garbage. Use GetContact or GetContacts instead.
---@field contactCount fun() @Gets the number of contacts for this collision.
---@field GetContacts fun() @Retrieves all contact points for this collision.
---@field body fun() @The Rigidbody or ArticulationBody of the collider that your Component collides with (Read Only).
---@field relativeVelocity fun() @The relative linear velocity of the two colliding objects (Read Only).
---@field GetContact fun() @Gets the contact point at the specified index.
