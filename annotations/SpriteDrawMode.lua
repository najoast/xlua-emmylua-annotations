---@class SpriteDrawMode @SpriteRenderer draw mode.
---@field Sliced fun() @The SpriteRenderer will render the sprite as a 9-slice image where the corners will remain constant and the other sections will scale.
---@field Tiled fun() @The SpriteRenderer will render the sprite as a 9-slice image where the corners will remain constant and the other sections will tile.
