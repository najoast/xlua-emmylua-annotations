---@class DeviceSimulation.TouchEvent @Representation of a single touch event coming from a Device Simulator. Subscribe to DeviceSimulator.touchScreenInput to receive these events.
---@field touchId fun() @The unique identifier for the touch. Unity reuses identifiers after the touch ends.
---@field phase fun() @Phase of the touch event.
---@field position fun() @On-screen position of the touch event. The zero point is at the bottom-left corner of the screen in pixel coordinates.
